    public void cargarJuegos(){
        
        Statement st;
        
        String sql = "select * from videojuego";
        
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("ID");
        model.addColumn("TITULO");
        model.addColumn("NOMBRE");
        model.addColumn("PROTAGONISTA");
        model.addColumn("DIRECTOR");
        model.addColumn("PRODUCTOR");
        model.addColumn("TECNOLOGIA");
        
        jTable1.setModel(model);
        
        String[] dato = new String[7];
        
        try {
            st = cn.createStatement();
            ResultSet result = st.executeQuery(sql);
            
            while(result.next()){
                dato[0] = result.getString(1);
                dato[1] = result.getString(2);
                dato[2] = result.getString(3);
                dato[3] = result.getString(4);
                dato[4] = result.getString(5);
                dato[5] = result.getString(6);
                dato[6] = result.getString(7);
                model.addRow(dato);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Alquileres.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

