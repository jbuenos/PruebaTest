    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        String id = id_alquiler.getText();
        String id_videojuego = id_Videojuego.getText();
        String id_usuario = id_Usuario.getText();
        String valor = precio.getText();
        String fechaAlquiler = fecha_alquiler.getText();
        String fechaEntrega = fecha_entrega.getText();
        
        if(id.equals("") && id_videojuego.equals("") && id_usuario.equals("") && fechaAlquiler.equals("AAAA-MM-DD")
                && valor.equals("")){
            JOptionPane.showMessageDialog(null, "Ning√∫n campo puede estar en blanco!");
        }else{
            
            try {
            PreparedStatement pps = cn.prepareStatement("INSERT INTO alquiler (cod_alquiler,cod_usuario, cod_videojuego, valor_alquiler, fecha_compra, fecha_entrega) VALUES (?,?,?,?,?,?);");
            pps.setString(1, id);
            pps.setString(2, id_usuario);
            pps.setString(3, id_videojuego);
            pps.setString(4, valor);
            pps.setString(5, fechaAlquiler);
            pps.setString(6, fechaEntrega);
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Alquiler generado exitosamente!");
            id_alquiler.setText("");
            id_Videojuego.setText("");
            id_Usuario.setText("");
            precio.setText("");
            fecha_alquiler.setText("AAAA-MM-DD");
            fecha_entrega.setText("AAAA-MM-DD");
            
        } catch (SQLException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error intentando guardar el usuario");
        }
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

